  "connect-src 'self' https://*.supabase.co https://api.groq.com https://api.resend.com https://api.pwnedpasswords.com https://ipapi.co",
  "frame-src 'self' https://www.termsfeed.com",
  "form-action 'self'",
  "upgrade-insecure-requests",
